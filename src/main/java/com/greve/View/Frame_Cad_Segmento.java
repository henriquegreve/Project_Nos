    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        if(Controller.logged == 3) {
            int input = JOptionPane.showConfirmDialog(null, "Deseja deletar o segmento?", "Deletar...", JOptionPane.YES_NO_OPTION);

            if (input == 0) {
                int id = Integer.parseInt(txtID.getText());

                if (Controller.deleteSegmentoById(id)) {
                    JOptionPane.showMessageDialog(null, "Segmento deletado com sucesso!");
                    limpaTela();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao deletar segmento!");
                }
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void txtIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtIDFocusLost
        // TODO add your handling code here:
        if(!txtID.getText().trim().equals("")){
            preencheTela(Integer.parseInt(txtID.getText()));
        }
    }//GEN-LAST:event_txtIDFocusLost

    private void preencheTela(int id) {
        Segmento seg = Controller.getSegmentoById(id);

        if(seg != null) {
            txtID.setText(String.valueOf(seg.getId()));
            txtDescricao.setText(seg.getDescricao());
            chbAtivo.setSelected(seg.isAtivo());
        } else {
            int input = JOptionPane.showConfirmDialog(null, "Novo Cadastro?",
                    "Deseja cadastrar novo Segmento?", JOptionPane.YES_NO_OPTION); //0 - YES | 1 - NO

            if (input == 0){
                txtID.setEnabled(false);
            }

            limpaTela();
        }
    }

