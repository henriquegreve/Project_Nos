    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        if(!txtID.getText().trim().equals("") ||
                !txtDescricao.getText().trim().equals("") ||
                chbAtivo.isSelected())
        {
            if(!txtID.isEnabled()) {
                txtID.setEnabled(true);
            }

            limpaTela();
        } else {
            this.dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void limpaTela() {
        txtID.setText("");
        txtDescricao.setText("");
        chbAtivo.setSelected(false);
    }

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        long id;
        String descricao = txtDescricao.getText();
        boolean ativo = chbAtivo.isSelected();

        if(txtID.getText().trim().equals("")) {
            if (!txtDescricao.getText().trim().equals("")) {
                id = Controller.createSegmento(descricao, ativo);

                if (id > 0) {
                    JOptionPane.showMessageDialog(null, "Segmento Cadastrado!");

                    txtID.setText(String.valueOf(id));

                    if (!txtID.isEnabled()) {
                        txtID.setEnabled(true);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Segmento ja cadastrado!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Digite as informacoes necessarias!");
            }
        } else {
            id = Long.parseLong(txtID.getText());

            if(Controller.updateSegmentoById(id, descricao, ativo) > 0){
                JOptionPane.showMessageDialog(null, "Segmento atualizado!");
            }
        }
    }//GEN-LAST:event_btnOKActionPerformed

