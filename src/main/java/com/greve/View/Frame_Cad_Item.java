    private void preencheTela(long id) {
        Items it = Controller.getItemById(id);

        if(it != null) {
            txtID.setText(String.valueOf(it.getId()));
            txtDescricao.setText(it.getDescricao());
            chbAtivo.setSelected(it.isAtivo());
            cbbSegmento.setSelectedIndex(it.getIdSegmento());
            cbbMarca.setSelectedIndex(it.getIdMarca());
        } else {
            int input = JOptionPane.showConfirmDialog(null, "Novo Cadastro?",
                    "Deseja cadastrar novo Usuario?", JOptionPane.YES_NO_OPTION); //0 - YES | 1 - NO

            if (input == 0){
                txtID.setEnabled(false);
            }

            limpaTela();
        }
    }

