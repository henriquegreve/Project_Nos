    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        if(!txtID.getText().trim().equals("") ||
                !txtDescricao.getText().trim().equals("") ||
                !(cbbSegmento.getSelectedIndex() == -1) ||
                !(cbbMarca.getSelectedIndex() == -1))
        {
            limpaTela();
        } else {
            this.dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void limpaTela() {
        txtID.setText("");
        chbAtivo.setSelected(false);
        txtDescricao.setText("");
        cbbSegmento.setSelectedIndex(-1);
        cbbMarca.setSelectedIndex(-1);
        txtEstoque.setText("");
    }

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        long id = 0;
        String descricao = txtDescricao.getText();
        boolean ativo = chbAtivo.isSelected();
        int idSegmento = cbbSegmento.getSelectedIndex();
        int idMarca = cbbMarca.getSelectedIndex();

        if(txtID.getText().trim().equals("")) {
            if (!descricao.trim().equals("")
                    && !(idSegmento == -1)
                    && !(idMarca == -1))
            {

                id = Controller.createItem(descricao, ativo, idSegmento, idMarca);

                if (id > 0) {
                    JOptionPane.showMessageDialog(null, "Item Cadastrado!");

                    txtID.setText(String.valueOf(id));

                    if (!txtID.isEnabled()) {
                        txtID.setEnabled(true);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Item ja cadastrado!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Digite as informacoes necessarias!");
            }
        } else {
            id = Long.parseLong(txtID.getText());

            if(Controller.updateItemById(id, descricao, ativo, idSegmento, idMarca) > 0){
                JOptionPane.showMessageDialog(null, "Usuario atualizado!");
            }
        }
    }//GEN-LAST:event_btnOKActionPerformed

