    private void limpaTela() {
        txtID.setText("");
        txtNome.setText("");
        txtLogin.setText("");
        txtPasswd.setText("");
        cbbNivel.setSelectedIndex(-1);
    }

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        if(!txtNome.getText().trim().equals("")
                || !txtLogin.getText().trim().equals("")
                || !String.valueOf(txtPasswd.getPassword()).trim().equals("")
                || cbbNivel.getSelectedIndex() != -1
                || !txtID.getText().trim().equals(""))
        {
            if(!txtID.isEnabled()) {
                txtID.setEnabled(true);
            }

            limpaTela();

        } else {
            this.dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        long id = 0;
        String nome = txtNome.getText();
        String username = txtLogin.getText();
        String passwd = String.valueOf(txtPasswd.getPassword());
        int level = cbbNivel.getSelectedIndex() + 1;

        if(txtID.getText().trim().equals("")) {
            if (!txtNome.getText().trim().equals("")
                    && !txtLogin.getText().trim().equals("")
                    && !String.valueOf(txtPasswd.getPassword()).trim().equals("")
                    && cbbNivel.getSelectedIndex() > -1) {
                id = Controller.createUser(nome, username, passwd, level);

                if (id > 0) {
                    JOptionPane.showMessageDialog(null, "Usuario Cadastrado!");

                    txtID.setText(String.valueOf(id));

                    if (!txtID.isEnabled()) {
                        txtID.setEnabled(true);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Usuario ja cadastrado!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Digite as informacoes necessarias!");
            }
        } else {
            id = Long.parseLong(txtID.getText());

            if(Controller.updateUserById(id, nome, username, passwd, level) > 0){
                JOptionPane.showMessageDialog(null, "Usuario atualizado!");
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void txtIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtIDFocusLost
        // TODO add your handling code here:
        if(!txtID.getText().trim().equals("")){
            preencheTela(Integer.parseInt(txtID.getText()));
        }
    }//GEN-LAST:event_txtIDFocusLost

