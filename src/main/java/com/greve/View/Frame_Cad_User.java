    private void preencheTela(int id){
       Users us = Controller.getUserById(id);

       if(us != null) {
           txtID.setText(String.valueOf(us.getId()));
           txtNome.setText(us.getNome());
           txtLogin.setText(us.getUsername());
           cbbNivel.setSelectedIndex(us.getLevel() - 1);
       } else {
           int input = JOptionPane.showConfirmDialog(null, "Novo Cadastro?",
                   "Deseja cadastrar novo Usuario?", JOptionPane.YES_NO_OPTION); //0 - YES | 1 - NO

           if (input == 0){
               txtID.setEnabled(false);
           }

           limpaTela();
       }
    }

